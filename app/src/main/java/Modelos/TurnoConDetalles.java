package Modelos;

public class TurnoConDetalles {
    public Turno turno;
    public String nombreBarbero;
    public String nombreServicio;

    public TurnoConDetalles(Turno turno, String nombreBarbero, String nombreServicio) {
        this.turno = turno;
        this.nombreBarbero = nombreBarbero;
        this.nombreServicio = nombreServicio;
    }
}
